package com.smdev.model;

/**
 * Supported types of data, that can be stored in a single data cell.
 *
 * @author Ireth
 */
public enum DataCellType {
	/** Decimal data */
	BIGDECIMAL,
	/** Integer data */
	BIGINTEGER,
	/** Boolean data */
	BOOLEAN,
	/** Date data */
	DATE,
	/** Strinf data */
	STRING;

}
